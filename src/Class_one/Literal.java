//
//        Example: char c = 'A';
//        String Literals: Represent a sequence of characters enclosed in double quotes.
//
//        Example: String s = "Hello, World!";
//        Boolean Literals: Represent truth values, either true or false.
//
//                Example: boolean d = true;
//        Null Literal: Represents the null reference, indicating that a variable does not point to any object.
//
//                Example: String e = null;
